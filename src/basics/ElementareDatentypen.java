package basics;

public class ElementareDatentypen {
	public static void main(String[] args) {
		/*
		 * Zahlentypen
		 */
		byte	x1;		//  8 bit	-128 ... 127 
		short	x2;		// 16 bit
		int		x3;		// 32 bit
		long	x4;		// 64 bit
		
		float	x5;		// 32 bit
		double	x6;		// 64 bit
		
		/*
		 * Zeichentypen
		 */
		char	x7;		// 16 bit
		
		/*
		 * Wahrheitstypen
		 */
		boolean	x8;		// nicht definiert
	}
}